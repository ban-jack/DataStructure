package com.data.leetcode.linkedlist;

// 定义一个单链表
public class ListNode {

    int val;
    com.data.leetcode.linkedlist.ListNode next;
    ListNode() {}
    ListNode(int val) { this.val = val; }
    ListNode(int val, com.data.leetcode.linkedlist.ListNode next) { this.val = val; this.next = next; }

}
